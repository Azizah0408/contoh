    @Override
    public void onBackPressed() {
        DrawerLayout drawer = (DrawerLayout) findViewById(R.id.drawer_layout);
        if (drawer.isDrawerOpen(GravityCompat.START)) {
            drawer.closeDrawer(GravityCompat.START);
        } else {
            AlertDialog.Builder a = new AlertDialog.Builder(this);
            a.setMessage("Apakah Anda Yakin Ingin Keluar?");
            a.setCancelable(true);

            a.setPositiveButton("Ya", new DialogInterface.OnClickListener() {
                @Override
                public void onClick(DialogInterface dialog, int which) {
                    finish();
                }
            });
            a.setNegativeButton("Tidak", new DialogInterface.OnClickListener() {
                @Override
                public void onClick(DialogInterface dialog, int which) {
                    Toast.makeText(MainActivity.this, "Terimaksih", Toast.LENGTH_LONG).show();
                }
            });
            AlertDialog alertDialog = a.create();
            a.show();
        }
    }



